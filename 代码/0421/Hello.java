
// 这是java的快速入门,演示java的开发步骤
// 对代码的相关说明

public class Hello{ // 表示Hello是一个类，是一个public公有的类
	// 编写一个main方法
	public static void main(String[] args){ // 表示一个主方法，即我们程序的入口;main(){}表示方法的开始和结束
		System.out.println("hello, world~"); // 表示打印输出内容
	}
} // {}表示一个类的开始和结束

class Dog{
	public static void main(String[] args){
		System.out.println("hello, dogs~");
	}
}


class Tiger{
	public static void main(String[] args){
		System.out.println("hello, tigers~");
	}
}




